    // INVERSE
    private void btnInverseActionPerformed(java.awt.event.ActionEvent evt) {
        if (!isError)
            if (Double.parseDouble(txtDisplay.getText()) == 0) {
                txtDisplay.setText("ERROR");
                isError = true;
            } else {
                txtDisplay.setText(convertDecimalFormat(1 / (Double.parseDouble(txtDisplay.getText()))));
                canAppend = false;
            }
    }

    // PERCENT
    private void btnPercentActionPerformed(java.awt.event.ActionEvent evt) {
        if (!isError) {
            txtDisplay.setText(convertDecimalFormat(Double.parseDouble(txtDisplay.getText()) / 100));
            canAppend = false;
        }
    }

    // SQUARE
    private void btnSqrtActionPerformed(java.awt.event.ActionEvent evt) {
        if (!isError) {
            if (Double.parseDouble(txtDisplay.getText()) < 0) {
                txtDisplay.setText("ERROR");
                isError = true;
            } else
                txtDisplay.setText(convertDecimalFormat(Math.sqrt(Double.parseDouble(txtDisplay.getText()))));
            canAppend = false;
        }
    }

    // CHANGE SIGN
    private void btnChangeSignActionPerformed(java.awt.event.ActionEvent evt) {
        if (!isError) {
            String result = txtDisplay.getText();
            if (canAppend && !result.equals("0"))
                if (result.charAt(0) != '-')
                    result = "-" + result;
                else
                    result = result.substring(1, result.length());
            if (!canAppend) {
                result = convertDecimalFormat(Double.parseDouble(result) * (-1));
                num = Double.parseDouble(result) * (-1);
            }
            txtDisplay.setText(result);
        }
    }

    // BACKSPACE
    private void btnBackspaceActionPerformed(java.awt.event.ActionEvent evt) {
        if (canAppend && !isError) {
            String result = txtDisplay.getText();
            int length = result.length();
            System.out.println(length);
            if (length > 3)
                if ((result.charAt(length - 1) + "").equals("."))
                    result = result.substring(0, length - 2);
                else
                    result = result.substring(0, length - 1);
            else if (length == 3)
                if (result.charAt(0) == '-' && result.charAt(2) == '.')
                    result = "0";
                else if ((result.charAt(length - 1) + "").equals("."))
                    result = result.substring(0, length - 2);
                else
                    result = result.substring(0, 2);
            else if (length == 2)
                if (result.charAt(0) == '-' || result.charAt(1) == '.')
                    result = "0";
                else
                    result = result.substring(0, 1);
            else
                result = "0";
            txtDisplay.setText(result);
        }
    }//GEN-LAST:event_btnBackspaceActionPerformed

